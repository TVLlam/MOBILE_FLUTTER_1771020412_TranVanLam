                            decoration: BoxDecoration(
                              color: AppColors.success.withOpacity(0.1),
                              borderRadius: BorderRadius.circular(12),
                            ),
                            child: const Icon(
                              Icons.calendar_today,
                              color: AppColors.success,
                              size: 24,
                            ),
                          ),
                          const SizedBox(width: 12),
